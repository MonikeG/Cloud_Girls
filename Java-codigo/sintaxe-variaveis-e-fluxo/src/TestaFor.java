
public class TestaFor {
	public static void main(String[] args) {
		for(int contador = 0; contador <= 10; contador ++) {
			System.out.println(contador);
		}
		//System.out.println(); não funciona porque o for só funciona dentro do seu proprio escopo, dependendo do contexto o while é melhor ou o for para contagens internas dentro do código
	}

}
