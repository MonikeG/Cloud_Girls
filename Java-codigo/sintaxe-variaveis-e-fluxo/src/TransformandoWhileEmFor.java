
public class TransformandoWhileEmFor {
	
	 public static void main(String[] args) {
	        int contador = 0;
	        while(contador <= 10) {
	            System.out.println(contador);
	            contador++;
	        }
	        
	        for(int contadores = 0; contadores <= 10; contadores++ ) {
	        	System.out.println(contadores);
	        }
	 }
}

